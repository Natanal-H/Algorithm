## 문제 정보
# **문제 이름**: 문자열 곱하기
# **문제 번호**: 181940
# **링크**: [링크](https://programmers.co.kr/learn/courses/30/lessons/181940)

## 문제 
# 문제 설명 :
# 문자열 my_string과 정수 k가 주어질 때, 
# my_string을 k번 반복한 문자열을 return 하는 solution 함수를 작성해 주세요.

## 코드
def solution(my_string, k):
    return my_string*k
