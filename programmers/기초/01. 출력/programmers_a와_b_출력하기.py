## 문제 정보
# **문제 이름**: a와 b 출력하기
# **문제 번호**: 181951
# **링크**: [링크](https://programmers.co.kr/learn/courses/30/lessons/181951)

## 문제 
# 문제 설명 :
# 정수 a와 b가 주어집니다. 각 수를 입력받아 입출력 예와 같은 형식으로 출력하는 코드를 작성해 보세요.

## 코드
a, b = map(int, input().strip().split(' '))
print(f"a = {a}\nb = {b}")
